  const cities = province ? Object.keys(regionData[province] || {}) : []
  const districts = province && city ? (regionData[province]?.[city] || []) : []

  const handleProvinceChange = (value) => {
    setProvince(value)
    setCity('')
    setDistrict('')
  }

